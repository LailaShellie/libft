#include "libft.h"

double				ft_cos(double rad)
{
	double x;
	double a;
	double b;
	int n;
	double c[8];

	n = 8;
	a = -0.5;
	b = 0.5;
	c[0] =	0.47200121631914989523;
	c[1] =	0;
	c[2] = 	-0.49940325881454302096;
	c[3] =	0;
	c[4] =	0.02799207946522214049;
	c[5] =	0;
	c[6] =	-0.00059664860812650323;
	c[7] =	0;
	x = rad / 3.141592653589793238462643;
	return (f_x(x, c, a, b, n));
}